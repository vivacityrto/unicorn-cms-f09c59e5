  ADD COLUMN IF NOT EXISTS generated_item_id text,
  ADD COLUMN IF NOT EXISTS generation_status text DEFAULT 'pending',
  ADD COLUMN IF NOT EXISTS last_error text,
  ADD COLUMN IF NOT EXISTS updated_by uuid,
  ADD COLUMN IF NOT EXISTS is_manual_allocation boolean NOT NULL DEFAULT false;

-- 1c. CREATE document_generation_errors
CREATE TABLE IF NOT EXISTS document_generation_errors (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  documentinstance_id bigint NOT NULL REFERENCES document_instances(id) ON DELETE CASCADE,
  error_code text,
  error_message text NOT NULL,
  error_detail jsonb,
  created_at timestamptz NOT NULL DEFAULT now(),
  resolved_at timestamptz,
  resolved_by uuid
);

CREATE INDEX IF NOT EXISTS idx_dge_instance ON document_generation_errors(documentinstance_id);
CREATE INDEX IF NOT EXISTS idx_dge_unresolved ON document_generation_errors(documentinstance_id) WHERE resolved_at IS NULL;

