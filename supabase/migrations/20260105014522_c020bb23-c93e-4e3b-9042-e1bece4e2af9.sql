-- 8. Backfill package_stages from tenants.stage_ids[] arrays
-- For each package in use, collect all stage_ids from tenants on that package
INSERT INTO public.package_stages (package_id, stage_id, sort_order, is_required, created_at)
SELECT DISTINCT 
  me.package_id,
  unnest_stage_id,
  0 as sort_order,
  true as is_required,
  now() as created_at
FROM public.membership_entitlements me
CROSS JOIN LATERAL (
  SELECT unnest(t.stage_ids::bigint[]) as unnest_stage_id
  FROM public.tenants t
  WHERE t.id = me.tenant_id
  AND t.stage_ids IS NOT NULL
  AND array_length(t.stage_ids::bigint[], 1) > 0
) stages
WHERE EXISTS (SELECT 1 FROM public.documents_stages ds WHERE ds.id = stages.unnest_stage_id)
ON CONFLICT (package_id, stage_id) DO NOTHING;

-- Also backfill from tenants.package_ids array
INSERT INTO public.package_stages (package_id, stage_id, sort_order, is_required, created_at)
SELECT DISTINCT 
  pkg_id,
  unnest_stage_id,
  0 as sort_order,
  true as is_required,
  now() as created_at
FROM public.tenants t
CROSS JOIN LATERAL unnest(t.package_ids::bigint[]) as pkg_id
CROSS JOIN LATERAL (
  SELECT unnest(t.stage_ids::bigint[]) as unnest_stage_id
) stages
WHERE t.stage_ids IS NOT NULL
AND t.package_ids IS NOT NULL
AND array_length(t.stage_ids::bigint[], 1) > 0
AND EXISTS (SELECT 1 FROM public.packages p WHERE p.id = pkg_id)
AND EXISTS (SELECT 1 FROM public.documents_stages ds WHERE ds.id = stages.unnest_stage_id)
ON CONFLICT (package_id, stage_id) DO NOTHING;

-- 9. Backfill client_package_stage_state for existing entitlements
-- Create rows for each tenant/package/stage combination based on package_stages
INSERT INTO public.client_package_stage_state (tenant_id, package_id, stage_id, status, is_required, sort_order, created_at)
SELECT 
  me.tenant_id,
  me.package_id,
  ps.stage_id,
  'not_started' as status,
  ps.is_required,
  ps.sort_order,
  now()
FROM public.membership_entitlements me
JOIN public.package_stages ps ON ps.package_id = me.package_id
WHERE NOT EXISTS (
  SELECT 1 FROM public.client_package_stage_state cpss 
  WHERE cpss.tenant_id = me.tenant_id 
  AND cpss.package_id = me.package_id 
  AND cpss.stage_id = ps.stage_id
)
ON CONFLICT (tenant_id, package_id, stage_id) DO NOTHING;

-- 10. Sync rto_number from tenant_profile to tenants.rto_id
