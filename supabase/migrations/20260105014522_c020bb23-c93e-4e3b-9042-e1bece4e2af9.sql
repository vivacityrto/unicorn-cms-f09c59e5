-- package_stages: readable by authenticated users, writable by admins
CREATE POLICY "package_stages_select_authenticated" ON public.package_stages
  FOR SELECT TO authenticated USING (true);

CREATE POLICY "package_stages_insert_authenticated" ON public.package_stages
  FOR INSERT TO authenticated WITH CHECK (true);

CREATE POLICY "package_stages_update_authenticated" ON public.package_stages
  FOR UPDATE TO authenticated USING (true);

CREATE POLICY "package_stages_delete_authenticated" ON public.package_stages
  FOR DELETE TO authenticated USING (true);

-- tenant_users: users can see their own tenant memberships
CREATE POLICY "tenant_users_select_own" ON public.tenant_users
  FOR SELECT TO authenticated 
  USING (user_id = auth.uid() OR EXISTS (
    SELECT 1 FROM public.tenant_users tu2 
    WHERE tu2.tenant_id = tenant_users.tenant_id 
    AND tu2.user_id = auth.uid() 
    AND tu2.role = 'parent'
  ));

CREATE POLICY "tenant_users_insert_parent" ON public.tenant_users
  FOR INSERT TO authenticated 
  WITH CHECK (EXISTS (
    SELECT 1 FROM public.tenant_users tu2 
    WHERE tu2.tenant_id = tenant_users.tenant_id 
    AND tu2.user_id = auth.uid() 
    AND tu2.role = 'parent'
  ) OR NOT EXISTS (
    SELECT 1 FROM public.tenant_users tu3 WHERE tu3.tenant_id = tenant_users.tenant_id
  ));

CREATE POLICY "tenant_users_delete_parent" ON public.tenant_users
  FOR DELETE TO authenticated 
  USING (EXISTS (
    SELECT 1 FROM public.tenant_users tu2 
    WHERE tu2.tenant_id = tenant_users.tenant_id 
    AND tu2.user_id = auth.uid() 
    AND tu2.role = 'parent'
  ));

