-- 3. Create package_stages table (package-stage catalog mapping)
CREATE TABLE IF NOT EXISTS public.package_stages (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  package_id bigint NOT NULL REFERENCES public.packages(id) ON DELETE CASCADE,
  stage_id bigint NOT NULL REFERENCES public.documents_stages(id) ON DELETE CASCADE,
  sort_order int NOT NULL DEFAULT 0,
  dashboard_group text NULL,
  is_required boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now(),
  created_by uuid NULL,
  UNIQUE(package_id, stage_id)
);

-- Add comment for clarity
COMMENT ON TABLE public.package_stages IS 'Maps which stages belong to which packages (catalog-level). Replaces tenants.stage_ids[] array usage.';
COMMENT ON COLUMN public.package_stages.dashboard_group IS 'Grouping for dashboard display: ONBOARDING, ONGOING_ACCESS, USAGE_SUPPORT, ANNUAL_OBLIGATIONS';

-- 4. Create tenant_users table (parent/child accounts per tenant)
CREATE TABLE IF NOT EXISTS public.tenant_users (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tenant_id bigint NOT NULL REFERENCES public.tenants(id) ON DELETE CASCADE,
  user_id uuid NOT NULL,
  role text NOT NULL DEFAULT 'child' CHECK (role IN ('parent', 'child')),
  created_at timestamptz NOT NULL DEFAULT now(),
  created_by uuid NULL,
  UNIQUE(tenant_id, user_id)
);

COMMENT ON TABLE public.tenant_users IS 'Tenant-scoped user memberships. Parent users can manage, child users have read-only access to tenant content.';

