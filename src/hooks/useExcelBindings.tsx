import { useState, useEffect, useCallback } from 'react';
import { supabase } from '@/integrations/supabase/client';
import { useToast } from '@/hooks/use-toast';

export interface TokenInfo {
  token: string;
  sheet: string;
  cell: string;
  context: 'cell' | 'header_footer' | 'named_range' | 'shape' | 'unknown';
  sample_text: string;
  format: 'double_brace' | 'double_angle' | 'double_bracket' | 'mergefield';
}

export interface DropdownInfo {
  dropdown_id: string;
  sheet: string;
  cell: string;
  validation_type: 'list';
  source_type: 'inline' | 'range' | 'named_range';
  source_ref: string;
  resolved_range: string | null;
  row_count_estimate: number | null;
  original_values: string[] | null;
}

export interface TokenBinding {
  source_type: 'client' | 'tenant' | 'package' | 'stage' | 'system' | 'static';
  source_field?: string;
  static_value?: string;
  preview_value?: string;
}

export interface DropdownBinding {
  list_id: string;
  list_name?: string;
}

export interface ExcelBinding {
  id: string;
  document_id: number;
  binding_version: number;
  status: 'draft' | 'ready' | 'error';
  detected_tokens: TokenInfo[];
  detected_dropdowns: DropdownInfo[];
  token_bindings: Record<string, TokenBinding>;
  dropdown_bindings: Record<string, DropdownBinding>;
  last_validated_at: string | null;
  validation_errors: ValidationError[];
  created_at: string;
  updated_at: string;
}

export interface ValidationError {
  type: 'unbound_token' | 'unbound_dropdown' | 'empty_list' | 'no_bindings';
  token?: string;
  dropdown_id?: string;
  location?: string;
  message: string;
}

export interface LookupList {
  id: string;
  tenant_id: number | null;
  key: string;
  name: string;
  description: string | null;
  created_at: string;
  items?: LookupListItem[];
}

export interface LookupListItem {
  id: string;
  list_id: string;
  value: string;
  label: string | null;
  sort_order: number;
  is_active: boolean;
}

